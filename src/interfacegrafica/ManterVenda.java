    private void txt_codProdutoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_codProdutoCaretUpdate
        String aux = txt_codProduto.getText();
        aux = aux.replace(" ", "");
        if (aux.length() == 13) {
            if (aux.equals("1111111111111")) {
                codigo = "1111111111111";
                descricao = "NISULID 10MG";
                valor = "19.00";
                subtotal = subtotal + Double.parseDouble(valor);
                IncluiItem();
            } else if (aux.equals("2222222222222")) {
                codigo = "2222222222222";
                descricao = "PARACETAMOL 6COMP 500MG";
                valor = "4.50";
                subtotal = subtotal + Double.parseDouble(valor);
                IncluiItem();
            } else if (aux.equals("3333333333333")) {
                codigo = "3333333333333";
                descricao = "GELOL TUB 120G";
                valor = "24.00";
                subtotal = subtotal + Double.parseDouble(valor);
                IncluiItem();
            } else {
                JOptionPane.showMessageDialog(null, "Código de produto inválido!");
                txt_codProduto.setText("");
                //txt_codProduto.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_codProdutoCaretUpdate

    public void IncluiItem() {
        DefaultTableModel val = (DefaultTableModel) tb_itens.getModel();
        val.addRow(new String[]{codigo, descricao, valor});
        lb_subtotal.setText(df.format(subtotal));
        AtualizaDesconto();
        AtualizaTotal();
        valor = "";
        txt_codProduto.setText("");
    }

    private void txt_cpfCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_cpfCaretUpdate
        String aux = txt_cpf.getText();
        aux = aux.replace(" ", "");
        if (aux.length() == 14) {
            if (aux.equals("111.111.111-11")) {
                lb_cliente.setText("LUIZ ANTONIO FREITAS JUNIOR");
                lb_convenio.setText("IPASGO");
                txDesconto = 5;
                lp_produtos.setEnabled(false);
                txt_codProduto.setEnabled(true);
            } else if (aux.equals("222.222.222-22")) {
                lb_cliente.setText("DANILO XAVIER TEIXEIRA DOS SANTOS");
                lb_convenio.setText("UNIMED");
                txDesconto = 10;
                txt_codProduto.setEnabled(true);
            } else if (aux.equals("000.000.000-00")) {
                lb_cliente.setText("VENDA AO CONSUMIDOR");
                txt_codProduto.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Cliente não cadastrado!");
                txt_cpf.setValue(null);
                txt_cpf.requestFocus();
                lb_cliente.setText("");
                lb_convenio.setText("");
                txt_codProduto.setEnabled(false);
            }
        } else {
            lb_cliente.setText("");
            lb_convenio.setText("");
        }
    }//GEN-LAST:event_txt_cpfCaretUpdate

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int retorno = JOptionPane.showConfirmDialog(null, "Deseja realmente cancelar a venda ?");
        if (retorno == JOptionPane.YES_OPTION) {
            txt_cpf.setText("");
            txt_codProduto.setText("");
            txt_codProduto.setEnabled(false);
            codigo = "";
            descricao = "";
            valor = "";
            subtotal = 0.00;
            desconto = 0.00;
            total = 0.00;
            txDesconto = 0.00;
            lb_subtotal.setText(df.format(0.00));
            lb_desconto.setText(df.format(0.00));
            lb_total.setText(df.format(0.00));
            lb_cliente.setText("");
            lb_convenio.setText("");
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(false);
            jRadioButton4.setSelected(false);

            DefaultTableModel tblRemove = (DefaultTableModel) tb_itens.getModel();
            int aux = tblRemove.getRowCount();
            if (aux > 0) {
                for (int i = 0; i <= aux; i++) {
                    tblRemove.removeRow(0);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

