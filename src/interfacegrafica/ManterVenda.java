    private void AtualizaDesconto() {
        if (txDesconto != 0) {
            desconto = ((subtotal / 100) * txDesconto);
            lb_desconto.setText(df.format(desconto));
        }
    }

    private void AtualizaTotal() {
        total = subtotal - desconto;
        lb_total.setText(df.format(total));
    }

